#pragma once

#include <memory>
#include <vector>
#include <string>
#include "src/core/RandomManager.h"
#include "src/character/Character.h"
#include "src/card/AbstractCard.h"

// ==========================================
// RunState - 持久层（一整局游戏）
//
// 生命周期：Act 1 → 通关心脏
//
// 包含内容：
// - masterDeck: 玩家总卡组（不是战斗中的抽牌堆）
// - gold/keys: 经济系统
// - relics/potions: 永久增益
// - currentHp/maxHp: 跨战斗继承的生命值
// - currentAct/currentFloor: 进度
// - rng: 地图生成和掉落用的 RNG
//
// 设计原则：
// - 纯数据容器，无业务逻辑
// - 战斗内获得的临时卡牌不存储在此
// - 药水消耗后直接从此列表移除
// ==========================================

class RunState {
public:
    // ==========================================
    // 日志开关
    // ==========================================
    bool enableLogging = true;

    // ==========================================
    // 玩家数据（持有 relics）
    // ==========================================
    std::shared_ptr<Player> player;

    // ==========================================
    // 经济系统
    // ==========================================
    int gold = 0;
    int keys = 0;

    // ==========================================
    // 永久卡组（不是战斗中的抽牌堆）
    // ==========================================
    std::vector<std::shared_ptr<AbstractCard>> masterDeck;

    // ==========================================
    // 进度
    // ==========================================
    int currentAct = 1;
    int currentFloor = 1;

    // ==========================================
    // 随机数（用于地图生成和掉落）
    // ==========================================
    RandomManager rng;

    // ==========================================
    // 药水（战斗中消耗后全局消失）
    // ==========================================
    std::vector<std::shared_ptr<AbstractPotion>> potions;

    // ==========================================
    // 构造函数
    // ==========================================
    RunState(unsigned int seed = 1337);

    // ==========================================
    // 核心接口
    // ==========================================

    // 检查玩家是否死亡
    bool isDead() const { return player->current_hp <= 0; }
};
